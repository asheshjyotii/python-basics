# Concatenation is the joining of two or more things in this case two or more things

string_1 = "i am\t"
string_2 = "Ashesh Jyoti Majumdar.\t"
string_3 = "I want to become a developer!"

print(string_1+string_2+string_3)

'''
Output

i am    Ashesh Jyoti Majumdar.  I want to become a developer!
'''